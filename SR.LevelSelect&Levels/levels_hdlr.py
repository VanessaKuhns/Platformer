class Button():
    def __init__(self, x, y, image):
        self.image = image
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.clicked = False
        
    def draw(self):
        action = False
        
        #mouse pos
        pos = pygame.mouse.get_pos()
        
        #clicked
        if self.rect.collidepoint(pos):
            if pygame.mouse.get_pressed()[0]==1 and self.clicked == False:
                action = True
                self.clicked = True
        
        if pygame.mouse.get_pressed()[0] == 0:
            self.clicked = False
        
        
        #draw button
        screen.blit(self.image, self.rect)
        
        return action
    


class Player():
    def __init__(self, x, y):
        self.reset(x, y)
                
    def update(self, game_over):
        dx = 0
        dy = 0
        walk_cooldown = 5
        if game_over ==  0:
            #get keypresses  
            key = pygame.key.get_pressed()
            if key[pygame.K_SPACE] and self.jump == False and self.in_air == False:
                self.vel_y = -15
                self.jump  = True
            if key[pygame.K_SPACE] == False:
                self.jump = False
            if key[pygame.K_LEFT]:
                dx -= 5
                self.counter += 1
                self.direction = -1
            if key [pygame.K_RIGHT]:
                dx += 3
                self.counter += 1
                self.direction = +1
            if key[pygame.K_LEFT == False] and key[pygame.K_RIGHT] == False:
                self.counter = 0
                self.index = 0
                if self.direction == 1:
                    self.image = self.images_right[self.index]
                if self.direction == -1:
                    self.image = self.images_left[self.index]
                
            #handle animation
            if self.counter > walk_cooldown:
                self.counter = 0
                self.index += 1
                if self.index >= len(self.images_right):
                    self.index = 0
                if self.direction == 1:
                    self.image = self.images_right[self.index]
                if self.direction == -1:
                    self.image = self.images_left[self.index]
            
            
            #add gravity
            self.vel_y += 1
            if self.vel_y > 15:
                self.vel_y = 10
            dy += self.vel_y        
            
            
            #check for collsions
            self.in_air = True
            for tile in world.tile_list:
                #horizontal collisions
                if tile[1].colliderect(self.rect.x + dx, self.rect.y, self.width, self.height ):
                    dx = 0
                #vertical collision
                if tile[1].colliderect(self.rect.x, self.rect.y + dy, self.width, self.height ):
                    #check if below ground/jumping
                    if self.vel_y < 0:
                        dy = tile[1].bottom - self.rect.top
                        self.vel_y = 0
                    #check above ground/falling
                    elif self.vel_y > 0:
                        dy = tile[1].top - self.rect.bottom
                        self.vel_y = 0
                        self.in_air = False
                
            #enemy collision
            if pygame.sprite.spritecollide(self, spike_group, False):
                game_over = -1
                
           #update player coordinates
            self.rect.x += dx
            self.rect.y += dy
            
            if self.rect.bottom > screen_height:
                self.rect.bottom = screen_height
                dy = 0
        elif game_over == -1:
            self.image = self.dead_image
            if self.rect.y > 10:
                self.rect.y -= 5
            
        #draw player on screen
        screen.blit(self.image, self.rect)
    
        return game_over
    
    def reset(self, x, y):
            self.images_right = []
            self.images_left = []
            self.index = 0
            self.counter = 0
            for num in range(1,6):
                img_right = pygame.image.load(f'SR.LevelSelect&Levels/char3/char3_run{num}.png')
                img_right = pygame.transform.scale(img_right, (15,29))
                img_left = pygame.transform.flip(img_right, True, False)
                self.images_right.append(img_right)
                self.images_left.append(img_left)
            self.dead_image = pygame.image.load('SR.LevelSelect&Levels/char3/char3_dead0.png')
            self.image = self.images_right[self.index]
            self.rect = self.image.get_rect()
            self.rect.x = x
            self.rect.y = y
            self.width = self.image.get_width()
            self.height = self.image.get_height()
            self.vel_y = 0
            self.jump = False 
            self.direction = 0
            self.in_air = True                 

class World():
    def __init__(self,data):
        self.tile_list = []
        
        
        #load images
        #1
        prpl_img = pygame.image.load('SR.LevelSelect&Levels/img/prpl.png')
        #2                 
        hlfblk_img = pygame.image.load('SR.LevelSelect&Levels/img/hlfblk.png')
        
        
        
        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(prpl_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count *tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile == 2:
                    img = pygame.transform.scale(hlfblk_img, (tile_size//4, tile_size //4))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count *tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile == 3:
                    spike = Enemy(col_count * tile_size, row_count * tile_size)
                    spike_group.add(spike)
                if tile == 4:
                    exit = Exit(col_count * tile_size, row_count * tile_size)
                    exit_group.add(exit)
                col_count += 1
            row_count += 1

